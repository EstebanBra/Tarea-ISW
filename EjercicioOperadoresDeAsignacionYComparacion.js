// Declaracion de variables
const numero1 = 15;
const numero2 = 20;
const numero3 = '25';

// Operadores de comparacion
console.log('¿El numero 1 es mayor o igual al numero 2?', numero1 >= numero2);
console.log('¿El numero 1 es menor o igual al numero 2', numero1 <= numero2);
console.log('¿El numero 1 es menor que el numero 3?', numero1 < numero3);
console.log('¿El numero 3 es menor que el numero 2', numero3 < numero2);
console.log('¿El numero 3 es estrictamente diferente que el numero 1', numero3 !== numero1);
console.log('¿El numero 1 es estrictamente igual al numero 2?', numero1 === numero2);